#====================================
# Descarga
#====================================

ticker = "BTC-USD"

data = yf.download(
    ticker,
    period="1y",
    interval="1d",
    progress=False
)

if data.empty:
    st.error("No fue posible descargar datos.")
    st.stop()

#====================================
# Precio de cierre
#====================================

close = data["Close"]

# Convertir a DataFrame si sólo existe un activo
if isinstance(close, pd.Series):
    close = close.to_frame(name=ticker)

#====================================
# Rendimientos logarítmicos
#====================================

returns = np.log(close / close.shift(1)).dropna()

#====================================
# Modelo GARCH
#====================================

vol_dict = {}

for col in returns.columns:

    model = arch_model(
        returns[col],
        mean="Zero",
        vol="GARCH",
        p=1,
        q=1,
        dist="normal"
    )

    result = model.fit(disp="off")

    vol_dict[col] = result.conditional_volatility

vol_df = pd.DataFrame(vol_dict)

#====================================
# Gráfico
#====================================

fig, ax = plt.subplots(figsize=(12,5))

for col in vol_df.columns:
    ax.plot(
        vol_df.index,
        vol_df[col] * 100,
        label=col
    )

ax.set_title("Volatilidad condicional GARCH(1,1)")
ax.set_xlabel("Fecha")
ax.set_ylabel("Volatilidad (%)")
ax.grid(True)
ax.legend()

st.pyplot(fig)

