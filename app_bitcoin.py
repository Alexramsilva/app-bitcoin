# ==========================================
# VARIABLES
# ==========================================

close = df["Close"]

volume = df["Volume"]

# ==========================================
# SOPORTE
# ==========================================

df["Support"] = close.rolling(
    window=ventana
).min().shift(1)

# ==========================================
# VOLUMEN PROMEDIO
# ==========================================

df["Vol_mean"] = volume.rolling(
    window=ventana
).mean()

# ==========================================
# RUPTURA
# ==========================================

df["Breakdown"] = (
    (close < df["Support"] * (1 - delta)) &
    (volume > df["Vol_mean"])
)

